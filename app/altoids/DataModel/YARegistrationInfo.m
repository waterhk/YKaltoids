#import "YARegistrationInfo.h"


@implementation YARegistrationInfo

@dynamic token;
@dynamic phoneNumber;
@dynamic profileUpdated;
@dynamic expirationDate;


- (NSString *)description
{
    return [NSString stringWithFormat:@"token:%@\nphoneNumber:%@\nprofileUpdated:%@\nexpirationDate:%@\n",[self token], [self phoneNumber],
            [self profileUpdated], [self expirationDate]];
}
@end
